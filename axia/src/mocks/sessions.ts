export const sessionById = (id: string): RehabSession => {
  const found = SESSIONS.find((s) => s.id === id)
  if (!found) throw new Error(`Unknown session: ${id}`)
  return found
}

export const sessionsForPatient = (patientId: string) =>
  SESSIONS.filter((s) => s.patientId === patientId).sort((a, b) =>
    b.date.localeCompare(a.date),
  )
